namespace AbcTuneTool.Model.Fields {

    /// <summary>
    ///     content kind for information fields
    /// </summary>
    public enum InformationFieldContent {

        /// <summary>
        ///     undefined content
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     default string content
        /// </summary>
        StringContent = 1,

        /// <summary>
        ///     instruction content
        /// </summary>
        Instruction = 2,

        /// <summary>
        ///     key description
        /// </summary>
        Key = 3,

        /// <summary>
        ///     note length
        /// </summary>
        NoteLength = 4,

        /// <summary>
        ///     meter
        /// </summary>
        Meter = 5,

        /// <summary>
        ///     macro definition
        /// </summary>
        Macro = 6,

        /// <summary>
        ///     origin of tune
        /// </summary>
        Origin = 7,

        /// <summary>
        ///     part values
        /// </summary>
        Parts = 8,

        /// <summary>
        ///     tempo definition
        /// </summary>
        Tempo = 9,

        /// <summary>
        ///     symbol line
        /// </summary>
        Symbols = 10,

        /// <summary>
        ///     user defined symbol shortcut
        /// </summary>
        UserDefined = 11,

        /// <summary>
        ///     voice information
        /// </summary>
        Voice = 12,
    }


}
