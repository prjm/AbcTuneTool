        StringContent = 1,

        /// <summary>
        ///     instruction content
        /// </summary>
        Instruction = 2,

        /// <summary>
        ///     key description
        /// </summary>
        Key = 3,

        /// <summary>
        ///     note length
        /// </summary>
        NoteLength = 4,

        /// <summary>
        ///     meter
        /// </summary>
        Meter = 5,
    }


}
